package edu.rseymour.advancedjava.interfaces;

/**
 * Interface to create a guitar neck.
 */

public interface Neck {

    /**
     * Straighten the neck to correct intonation.
     */
    public void straighten();

    /**
     * Dress the frets to remove dirt, increase playability, and improve
     * intonation.
     */
    public void dressFrets();
}
